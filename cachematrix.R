makeCacheMatrix <- function(x = matrix()) {
        m <- NULL
        set <- function(y) {
          x <<- y
          m <<- NULL
        }
        get <- function() x
        setinverse <- function(inv) m <<- inv    #Function named setinverse to set the inverse
        getinverse <- function() m               #Function named setinverse to get the inverse
        list(set = set, get = get,
             setinverse = setinverse,
             getinverse = getinverse)
}


